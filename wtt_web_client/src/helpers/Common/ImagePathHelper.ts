export default class ImagePathHelper
{
	public static get LeftArrow()
	{
		return window.location.origin + '/heroicons/solid/arrow-left.svg';
	}
	public static get RightArrow()
	{
		return window.location.origin + '/heroicons/solid/arrow-right.svg';
	}
	public static get DownArrow()
	{
		return window.location.origin + '/heroicons/solid/arrow-up.svg';
	}
	public static get UpArrow()
	{
		return window.location.origin + '/heroicons/solid/arrow-down.svg';
	}
}